int	ft_str_is_printable(char *src)
{
	int	i;

	i = 0;
	if (*src == '\0')
		return (1);
	while (*(src + i) != '\0')
	{
		if (!(*(src + i) >= 32 && *(src + i) <= '~'))
			return (0);
		i++;
	}
	return (1);
}
